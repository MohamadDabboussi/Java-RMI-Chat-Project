    private void signupbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupbuttonActionPerformed
        // TODO add your handling code here:
        try {
            String image = path.substring(path.lastIndexOf("/") + 1);
            if (!TextUsernameSignUp.getText().equals("") && !TextPasswordSignUp.getText().equals("")) {
                if(serverRef.CheckUsernameTaken(TextUsernameSignUp.getText())==false){
                User user = new User(TextUsernameSignUp.getText(), TextPasswordSignUp.getText(), TextAboutSignUp.getText(), image);
                serverRef.signUp(user);
                PanelSinUp.setVisible(false);
                signUp = false;
                }
                else{
                JOptionPane.showMessageDialog(null, "Username is taken");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please fill the empty fields");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_signupbuttonActionPerformed

