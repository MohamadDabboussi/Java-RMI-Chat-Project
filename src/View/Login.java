    private void SigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SigninActionPerformed
        //check user
        boolean correct=false;
        User user = new User(TextUsername.getText(), TextPassword.getText());
        try {
            correct=serverRef.signIn(user);
        } catch (RemoteException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (correct == true){
            DataLayer dl=new DataLayer();
            user.setId(User.CheckUser(dl, user.getUsername()));
            user.GetUserDataByID(dl);
            ClientGui c=new ClientGui(user);
            c.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Wrong combination. Please try again !");
        }
        // connect client
    }//GEN-LAST:event_SigninActionPerformed

